//imprimir la circunferencia, area y diametro.
#include <iostream>
using namespace std;

int main(){
    int radio,pi;
    radio=4;
    pi=3,14159;

    cout<<"el diametro es: "<<radio*2<<endl;
    cout<<"la circunferencia es: "<<2*pi*radio<<endl;
    cout<<"el area es: " <<pi*radio*radio<<endl;
    
}